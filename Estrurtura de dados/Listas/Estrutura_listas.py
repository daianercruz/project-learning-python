#Exemplo de criação de listas

frutas = ["laranja", "mamão", "banana"]
print(frutas)

frutas = []
print(frutas)

letras = list("python") #irá listar todas as letras da palavra Python
print(letras)

numeros = list(range(10))
print(numeros)

carros = ["Ferrari", "Enzo ferrari", 420000, 2020, 2000, "São Paulo", True]
print(carros)